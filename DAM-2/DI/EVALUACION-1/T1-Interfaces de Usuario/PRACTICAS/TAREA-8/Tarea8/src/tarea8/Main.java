    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (comprobarApellido1()) {
            mensajeError("Primer Apellido");
        } else if (comprobarApellido2()) {
            mensajeError("Segundo Apellido");
        } else if (comprobarNombre()) {
            mensajeError("Nombre");
        } else if (comprobarEquipo()) {
            mensajeError("Equipo");
        } else {

            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            String[] row = {TFApellido1.getText(), TFApellido2.getText(), TFNombre.getText(), jComboBox1.getSelectedItem().toString()};
            modelo.addRow(row);

            limpiar();

            JOptionPane jop = new JOptionPane("Jugador Guardado con Exito", JOptionPane.INFORMATION_MESSAGE);
            javax.swing.JDialog jd = jop.createDialog("INFO");
            jd.setLocationRelativeTo(this);
            jd.setVisible(true);
            jd.setAlwaysOnTop(true);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

