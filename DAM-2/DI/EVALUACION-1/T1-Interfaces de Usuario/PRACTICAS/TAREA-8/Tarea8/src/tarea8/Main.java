    private boolean comprobarApellido1() {
        if (TFApellido1.getText().isBlank()) {
            return true;
        } else if (TFApellido1.getText().matches(".*\\d.*")) {
            return true;
        } else {
            return false;
        }
    }

    private boolean comprobarApellido2() {
        if (TFApellido2.getText().isBlank()) {
            return true;
        } else if (TFApellido2.getText().matches(".*\\d.*")) {
            return true;
        } else {
            return false;
        }
    }

    private boolean comprobarNombre() {
        if (TFNombre.getText().isBlank()) {
            return true;
        } else if (TFNombre.getText().matches(".*\\d.*")) {
            return true;
        } else {
            return false;
        }
    }

    private boolean comprobarEquipo() {
        if (jComboBox1.getSelectedIndex() == 0) {
            return true;
        } else {
            return false;
        }
    }

    private void mensajeError(String fallo) {
        JOptionPane jop = new JOptionPane("Error en :" + fallo, JOptionPane.ERROR_MESSAGE);
        javax.swing.JDialog jd = jop.createDialog("ERROR");
        jd.setLocationRelativeTo(this);
        jd.setVisible(true);
        jd.setAlwaysOnTop(true);
    }

    private void limpiar() {
        TFApellido1.setText("");
        TFApellido2.setText("");
        TFNombre.setText("");
        jComboBox1.setSelectedIndex(0);

    }

